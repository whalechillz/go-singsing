#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="🚨 긴급수정: 티타임 관리 시스템 주요 버그 수정

수정사항:
1. ✅ 코스별 색상 구분 추가
   - 파인코스: 녹색
   - 레이크코스: 파란색
   - 힐스코스: 주황색
   - 기타 코스별 고유 색상

2. ✅ 남녀 참가자 표시 기능 강화
   - 남성: 파란색, 여성: 분홍색 표시
   - 혼성팀 자동 표시
   - 인원 통계 표시 (남X, 여Y)

3. ✅ 코스 정렬 순서 수정
   - 파인 → 레이크 → 힐스 우선 정렬
   - 나머지 코스는 알파벳순

4. ✅ 선택 삭제 기능 추가
   - 체크박스로 다중 선택
   - 선택된 티타임 일괄 삭제

5. ✅ 미리보기 오류 수정
   - 날짜/코스 필드명 호환성 개선
   - 에러 처리 강화

6. ✅ 날짜별 참가자 통계 표시
   - 총 인원, 남녀 구분 통계"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
