#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="feat: 참가자 관리 개선

수정사항:
1. ✅ 엑셀 템플릿 개선
   - 직책 필드 추가 (기본값: 총무)
   - 파일명에 투어명 포함
   - 템플릿 순서 조정

2. ✅ 간단 입력 기능 추가
   - 탭 구분 텍스트 붙여넣기 지원
   - 엑셀에서 복사한 데이터 한번에 입력
   - 직책 기본값 총무로 설정

3. ✅ 출발시간 필드 제거
   - DB 스키마에 없는 pickup_time 필드 제거
   - 참가자 정보에 탑승지만 표시

4. ✅ 투어별 페이지 기능 개선
   - 간단 입력 버튼은 투어별 페이지에서만 표시
   - 엑셀 업로드 시 '일괄결제' 필드 처리"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
