#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="🔧 투어 생성 오류 수정 및 간소화

수정사항:
1. ✅ 투어 생성 오류 수정
   - 데이터베이스 스키마에 없는 필드들 제거
   - 예약 안내 사항 필드 제거 (여행상품의 이용안내로 대체)
   - 문서 설정 탭 제거 (스키마에 없음)

2. ✅ 여행상품과의 연동 개선
   - tour_product_id 필드 추가
   - 여행상품 선택 시 자동으로 정보 채워짐
   - 포함/불포함 사항도 여행상품에서 가져옴

3. ✅ 숙소 입력 개선
   - 여러 숙소 입력 가능하도록 안내 문구 추가
   - 쉼표로 구분하여 입력 가능

4. ✅ 사용자 경험 개선
   - 불필요한 입력 필드 제거
   - 안내 메시지 추가"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
