#!/bin/bash

# Git 커밋 스크립트 - 비로그인 사용자 뷰 개선

echo "🚀 비로그인 사용자 뷰 개선 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --porcelain

# 스테이징
echo "📦 파일 스테이징 중..."
git add app/page.tsx

# 커밋
echo "💾 커밋 생성 중..."
git commit -m "feat: 비로그인 사용자를 위한 투어 일정표 미리보기 개선

- 비로그인 사용자에게는 투어 일정표만 표시
- 투어 기본 정보 미리보기 제공 (날짜, 골프장, 숙박, 가격)
- 로그인 유도 메시지와 추가 서류 안내 추가
- 사용자별 콘텐츠 분리 (비로그인/로그인/스탭)"

echo "✅ 커밋 완료!"

# 푸시 여부 확인
read -p "원격 저장소에 푸시하시겠습니까? (y/n) " -n 1 -r
echo
if [[ $REPLY =~ ^[Yy]$ ]]
then
    echo "🚀 원격 저장소에 푸시 중..."
    git push
    echo "✅ 푸시 완료!"
else
    echo "⏸️  푸시를 건너뛰었습니다."
fi

echo "🎉 모든 작업이 완료되었습니다!"
