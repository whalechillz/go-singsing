echo ""

# Vercel CLI가 설치되어 있는지 확인
if command -v vercel &> /dev/null; then
  echo "Vercel CLI를 사용하여 배포 상태를 확인합니다..."
  vercel ls --scope singsinggolf 2>/dev/null || echo "Vercel CLI 인증이 필요할 수 있습니다."
else
  echo "⚠️  Vercel CLI가 설치되어 있지 않습니다."
  echo "   Vercel 대시보드에서 배포 상태를 확인하세요: https://vercel.com"
