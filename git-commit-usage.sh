#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="feat: 여행상품 이용 안내 편집 기능 개선

주요 변경사항:
1. ✅ 락카 이용 위치 조정
   - 문서 미리보기에서 락카 이용을 식사 안내와 버스 이용 사이로 이동
   - 상단/하단 이용 안내 섹션 모두 동일하게 적용

2. ✅ '제목: 내용' 볼드체 기능 추가
   - formatTextWithBold 함수로 콜론 앞 텍스트를 자동으로 볼드 처리
   - 여행상품 편집 페이지에 실시간 미리보기 추가
   - 문서 미리보기에서도 동일한 포맷팅 적용

3. ✅ 사용자 경험 개선
   - 편집 시 입력 형식 안내 문구 추가
   - UsagePreview 컴포넌트로 즉시 결과 확인 가능"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
