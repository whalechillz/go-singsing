#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="refactor: 여행상품 관리 용어 변경

주요 변경사항:
1. ✅ '이용 안내 및 주의사항' → '예약 안내 사항'으로 변경
   - 여행상품 편집 페이지의 general_notices 섹션 제목 변경
   - 문서 미리보기의 동일 섹션 제목 변경

2. ✅ '이용 안내' → '상세 이용 안내'로 변경
   - 여행상품 편집 페이지의 usage 섹션 제목 변경
   - 문서 미리보기의 동일 섹션 제목 변경

3. 변경 이유
   - 더 명확한 용어 사용으로 사용자 혼동 방지
   - 섹션별 역할 구분 명확화"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
