#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "🚀 Git 커밋 시작..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="refactor: 투어 문서 메뉴명 간소화 및 용어 통일

주요 변경사항:
1. ✅ 문서 메뉴명 간소화
   - '고객용 일정표' → '일정표'
   - '고객용 탑승안내서' → '탑승안내 (고객용)'
   - '스탭용 탑승안내서' → '탑승안내 (스탭용)'
   - '객실 배정표 (고객용)' → '객실배정 (고객용)'
   - '객실 배정표 (스탭용)' → '객실배정 (스탭용)'
   - '간편 일정표' → '간편일정'

2. ✅ 용어 통일
   - '티타임표 (내부용)' → '티타임표 (스탭용)'
   - 모든 '내부용' 표현을 '스탭용'으로 통일
   - 문서 내 주석 및 설명 텍스트까지 일관성 있게 변경

3. 변경 이유
   - 메뉴명을 더 간결하게 만들어 UI 가독성 향상
   - 고객용/스탭용 구분을 일관된 형식으로 표현
   - '내부용'보다 '스탭용'이 더 명확한 의미 전달"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 커밋 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
