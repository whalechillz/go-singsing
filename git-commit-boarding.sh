#!/bin/bash

# Git 변경사항 커밋 스크립트

echo "📅 고객용 탑승안내서 시간 표시 개선..."

# 변경사항 확인
echo "📋 변경된 파일:"
git status --short

# 모든 변경사항 추가
git add .

# 커밋 메시지
COMMIT_MSG="feat: 고객용 탑승안내서 시간 표시 및 정렬 개선

주요 변경사항:
1. 📊 이동 경로 테이블 정렬 개선
   - 시간 컬럼: 가운데 정렬
   - 장소 컬럼: 왼쪽 정렬 (가독성 향상)
   - 헤더: 가운데 정렬

2. ⏰ 경유지 예상 시간 표시 추가
   - 기존: '-' 표시
   - 개선: '09:30경' 형식으로 예상 시간 표시
   - 계산식: 마지막 탑승지 출발 시간 + 30분 + (경유지 순서 × 10분)

3. 🎯 고객 편의성 향상
   - 경유지 도착 예상 시간으로 일정 파악 용이
   - '경' 표시로 대략적인 시간임을 명시
   - 60대 고객도 쉽게 이해할 수 있는 표시

📝 calculateWaypointTime 함수 추가
- 이전 출발 시간과 이동 시간을 기반으로 예상 시간 계산
- 24시간 형식 처리
- 오류 발생 시 '-' 반환"

# 커밋 실행
git commit -m "$COMMIT_MSG"

# 원격 저장소로 푸시
echo "📤 원격 저장소로 푸시 중..."
git push

echo "✅ 고객용 탑승안내서 개선 완료!"
echo "🌐 Vercel에서 자동 배포가 시작됩니다..."
